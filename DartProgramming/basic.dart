void main() {
  var num1 = 15;
  var num2 = 20;

  // Sum
  var sum = num1 + num2;
  // Extraction
  var diff = num1 - num2;
  // Multiply
  var mul = num1 * num2;
  // Div
  var div = num1 / num2;

  print("The sum is $sum.");
  print("The difference is $diff.");
  print("The multiplication is $mul.");
  print("The division is $div.");
}